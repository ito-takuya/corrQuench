    echo "CIFTI resample and parcellating (with Glasser parcels) data for subject ${subjNum}"
    subjDirOutput=${basedir}/data/hcppreprocessedmsmall/${subjNum}
    if [ "`ls -d $subjDirOutput`" == "" ]; then mkdir -p $subjDirOutput; fi	#Making directory if it doesn't exist yet

    echo "Processing on task runs..."
    for runName in $taskRuns; do
        subjRunDir=${datadir}/${subjNum}/MNINonLinear/Results/${runName}

        ## First do RL run
        pushd ${subjRunDir}_RL
